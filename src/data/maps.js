export const getMapBySlug = (slug) => maps.find(map => map.slug === slug);
export const getRecentMaps = (limit = 6) =>
  [...maps].sort((a, b) => new Date(b.createdDate) - new Date(a.createdDate)).slice(0, limit);
export const getPopularMaps = (limit = 6) =>
  [...maps].sort((a, b) => b.stats.plays - a.stats.plays).slice(0, limit);

